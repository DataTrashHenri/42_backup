void	ft_div_mod(int a, int b, int *div, int *mod)
{
	int		*a_by_b;
	int		*a_mod_b;

	a_by_b = div;
	a_mod_b = mod;
	*a_by_b = a / b;
	*a_mod_b = a % b;
}
